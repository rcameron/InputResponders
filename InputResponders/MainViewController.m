#pragma mark - IBAction methods
- (IBAction)showPickerResponderView {
    PickerResponderView *pickerView = [[PickerResponderView alloc] initWithFrame:self.view.frame];
    [self.view addSubview:pickerView];
    
    [pickerView becomeFirstResponder];
    
    [pickerView release];
}

